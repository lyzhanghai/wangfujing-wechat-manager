package com.wfj.dto;

/**
 * 视图型菜单事件
 *
 * @author kongqf
 * @create 2016-11-23
 */
public class ViewButton {
    private String type;
    private String name;
    private String url;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}